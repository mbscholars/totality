    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Character $character, CharacterStoreRequest $request)
    {
        (new CharacterRepository)->store($request->collect());

        return new CharacterCollection($character->refresh());
    }
    
